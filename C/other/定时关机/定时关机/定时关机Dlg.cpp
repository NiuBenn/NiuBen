tm* getnowtime()
{

	int hour = 0;
	int min = 0;
	time_t now;
	struct tm *timenow;
	time(&now);
	timenow = localtime(&now);
	return timenow;
}

void setshutdowntime(int hour, int min)
{

	//system("shutdown -a");
	tm* timenow = getnowtime();
	int nowhour = timenow->tm_hour;
	int nowmin = timenow->tm_min;
	int nowsec = timenow->tm_sec;
	
	if(hour < nowhour)
	{
		hour = hour + 24;
	}

	int time = (hour - nowhour)*3600 + (min - nowmin)*60 - nowsec-1;
	char shutdown[50];
	sprintf(shutdown, "shutdown -s -t %d", time);
	system(shutdown);
}

